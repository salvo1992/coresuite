        <h4>Documenti da allegare</h4>
        <strong>(prima del parto)</strong><br>
        - documento identità della richiedente-
        certificato ginecologo telematico, medico di struttura pubblica<br>
        - autorizzazione rilasciata dalla DTL o dalla ASL (in caso di precedente
        astensione anticipata)<br>
        - nei casi di maternità pagata da INPS, occorre il mod SR163 (scaricare l'ultima
        versione dal sito INPS)<br>
        Se si chiede il posticipo, occorre specifica dichiarazione del datore di lavoro (o del
        medico aziendale) e del ginecologo. Tale dichiarazione è da produrre al momento
        della domanda e non successivamente.<br>

        <strong>(dopo il parto)</strong><br>
        da presentare al piu' presto presso l'ufficio ove è presentata la domanda relativa al
        periodo pre-parto<br>
        - codice fiscale del/la nato/a rilasciato dall'Agenzia delle Entrate<br>
        - nei casi di estensione prolungamento al 7°mese: autorizzazione ITL certificato di
        assistenza al parto (da richiedersi dopo la nascita del figlio direttamente all'ITL)


